public class Task002Variables {
    public static void main(String[] args) {
        boolean myBool = false;
        char myChar = 'x';
        byte myByte = 0;
        short myShort = 432;
        int myInt = 4212;
        long myLong = 98412;
        float myFloat = 4f;
        double myDouble = 12f;
        String myString = "My string text.";

        System.out.println("My bool variable is: " + myBool);
        System.out.println("My char variable is: " + myChar);
        System.out.println("My byte variable is: " + myByte);
        System.out.println("My short variable is: " + myShort);
        System.out.println("My int variable is: " + myInt);
        System.out.println("My long variable is: " + myLong);
        System.out.println("My float variable is: " + myFloat);
        System.out.println("My double variable is: " + myDouble);
        System.out.println("My String variable is: " + myString);
    }
}
