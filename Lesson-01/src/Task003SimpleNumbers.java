public class Task003SimpleNumbers {
    public static void main(String[] args) {
        int question1 = 2815 + 3276;
        int question2 = 42389 - 2403;
        int question3 = 279 / 9;
        int question4 = 37 * 21;
        int question5 = 1675 % 50;

        System.out.println("2815 + 3276 = " + question1);
        System.out.println("42389 - 2403 = " + question2);
        System.out.println("279 / 9 = " + question3);
        System.out.println("37 * 21 = " + question4);
        System.out.println("1675 % 50 = " + question5);
        System.out.println("The % operator calculates the remainder when dividing the first number by the second number.");
    }
}
