public class Task004OtherOperators {
    public static void main(String[] args) {
        int myInt = 30;
        System.out.println("My initial value: " + myInt);

        myInt++;
        System.out.println("My int with ++: " + myInt);

        myInt--;
        System.out.println("My int with --: " + myInt);

        myInt+=10;
        System.out.println("My int with +=10: " + myInt);

        myInt-=10;
        System.out.println("My int with -=10: " + myInt);
    }
}
